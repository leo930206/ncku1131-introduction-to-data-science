# -------------------------------------------------------------
# 函式：expand_user_ingredients
# 說明：
#   1. 接收使用者輸入的食材清單。
#   2. 如果遇到同義詞，則依據 synonyms_map 一併加入擴充過的食材字串。
#   3. 回傳「所有輸入食材 + 同義詞」的集合，用來後續比對食譜。
# -------------------------------------------------------------
def expand_user_ingredients(user_ingredients):
    expanded = set()
    for ing in user_ingredients:
        ing_lower = ing.strip().lower()       # 先轉小寫，避免大小寫不一致
        if ing_lower in synonyms_map:
            # 如果在同義詞字典內，則將它對應的清單全部加到 expanded 裡
            for syn in synonyms_map[ing_lower]:
                expanded.add(syn.lower())
        else:
            # 若無在字典內，就直接加它原本的名稱（小寫）
            expanded.add(ing_lower)
    return expanded

# -------------------------------------------------------------
# 函式：find_recipes
# 說明：
#   1. 帶入已擴充好的「使用者擁有食材」集合。
#   2. 與所有食譜做比對，紀錄已使用的與缺少的食材。
#   3. 只列出「部分符合」的食譜，並排序（已符合多者優先，缺少少者優先）。
#   4. 最多只回傳前15筆結果。
# -------------------------------------------------------------
def find_recipes(available_ingredients):
    # result 用於儲存符合條件的食譜清單
    result = []
    for recipe in recipes:
        required_list = recipe["食材"]

        used_ings = []      # 用來保存「配對成功」的食材字串
        missing_items = []

        # 逐一檢查該食譜需要的每個食材
        for item in required_list:
            item_lower = item.lower()
            matched = False
            # 與使用者的「已擴充食材集合」做比對
            for ing in available_ingredients:
                if ing in item_lower:
                    used_ings.append(item)   # 加入原始字串，方便顯示
                    matched = True
                    break
            # 如果該 item 沒有對應成功，就歸到缺少食材中
            if not matched:
                missing_items.append(item)

        used_count = len(used_ings)
        total_need = len(required_list)

        # 只顯示「部分符合」：已符合數 > 0 且 < 該食譜需要的總食材數量
        if 0 < used_count < total_need:
            recipe_data = {
                "食物": recipe["食物"],
                "步驟": recipe["步驟"],
                "份量": recipe.get("份量", "未提供"),
                "時間": recipe.get("時間", "未提供"),
                "缺少的食材": missing_items,
                "缺少幾樣": len(missing_items),
                "used_count_internally": used_count,
                "matched_items": used_ings  # 為了顯示出已使用的食材清單
            }
            result.append(recipe_data)

    # 以「已符合多者優先，再依缺少較少者優先」排序
    result.sort(key=lambda r: (-r["used_count_internally"], r["缺少幾樣"]))
    return result[:15]

# -------------------------------------------------------------
# 函式：display_recipes
# 說明：
#   1. 接收由 find_recipes() 取得的食譜清單。
#   2. 透過 tkinter 的 scrolledtext 介面，詳細顯示每道食譜資訊。
#   3. 其中包含「缺少幾樣」、「缺少的食材」以及「擁有的食材」等。
# -------------------------------------------------------------
def display_recipes(recipes_list):
    # 創建一個新的 Toplevel 視窗，用於顯示結果
    result_window = tk.Toplevel(root)
    result_window.title("推薦食譜")

    # 設定字體
    large_font = font.Font(family="ZCOOL", size=16, weight="bold")
    medium_font = font.Font(family="ZCOOL", size=12)
    small_font = font.Font(family="ZCOOL", size=10)

    # 使用 ScrolledText 來支援滾動條
    text_area = scrolledtext.ScrolledText(result_window, wrap='word', width=80, height=30)
    text_area.pack(padx=10, pady=10, fill=tk.BOTH, expand=True)

    # 設定標籤格式
    text_area.tag_configure("title", font=("ZCOOL", 16, "bold"), foreground="blue")
    text_area.tag_configure("ingredients", font=("Helvetica", 13, "bold"), foreground="maroon")
    text_area.tag_configure("quantity_time", font=("Helvetica", 11), foreground="black")
    text_area.tag_configure("steps", font=("Helvetica", 14, "bold"), foreground="green")
    text_area.tag_configure("warning", font=("Helvetica", 12, "bold"), foreground="red")

    # 逐筆顯示符合的食譜
    for recipe in recipes_list:
        # 料理名稱
        text_area.insert(tk.END, f"料理名稱: {recipe['食物']}\n", "title")

        # 顯示「缺少幾樣」和「缺少的食材」
        missing_count = recipe.get("缺少幾樣", 0)
        text_area.insert(tk.END, f"缺少 {missing_count} 樣食材: ", "warning")
        text_area.insert(tk.END, ", ".join(recipe["缺少的食材"]) + "\n\n", "warning")

        # 份量（若有提供）
        if recipe.get('份量') and recipe['份量'] != "未提供":
            text_area.insert(tk.END, f"份量: {recipe['份量']}\n", "quantity_time")

        # 時間（若有提供）
        if recipe.get('時間') and recipe['時間'] != "未提供":
            text_area.insert(tk.END, f"時間: {recipe['時間']}\n", "quantity_time")

        # 顯示烹飪步驟
        text_area.insert(tk.END, "步驟:\n", "steps")
        for idx, step in enumerate(recipe["步驟"], 1):
            text_area.insert(tk.END, f"{idx}. {step}\n")

        # 顯示「已符合的食材」
        matched_items = recipe.get("matched_items", [])
        if matched_items:
            text_area.insert(tk.END, "\n目前擁有的食材：\n", "ingredients")
            for i, m in enumerate(matched_items, 1):
                text_area.insert(tk.END, f"  {i}. {m}\n", "ingredients")

        text_area.insert(tk.END, "\n")  # 分隔

    # 設置 Text 為只讀
    text_area.config(state='disabled')

# -------------------------------------------------------------
# 函式：on_submit
# 說明：
#   1. 取得使用者輸入的文字並以空格分割成食材清單。
#   2. 透過 expand_user_ingredients 擴充同義詞。
#   3. 以 find_recipes() 查詢「部分符合」的食譜。
#   4. 無符合則提示訊息；有則呼叫 display_recipes 顯示前15筆。
# -------------------------------------------------------------
def on_submit():
    user_ingredients_input = entry.get().split()
    if not user_ingredients_input:
        messagebox.showinfo("提醒", "請至少輸入一種食材。")
        return

    # 擴充同義詞
    expanded_ingredients = expand_user_ingredients(user_ingredients_input)
    # 查詢符合的食譜清單
    suggested_recipes = find_recipes(expanded_ingredients)

    if suggested_recipes:
        # 若有找到符合條件的食譜，顯示結果
        display_recipes(suggested_recipes)
    else:
        # 若沒有，則提示訊息
        messagebox.showinfo("推薦食譜", "很抱歉，根據你提供的食材找不到相關的食譜。")

